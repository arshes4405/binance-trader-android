                )
            )
        }
    ) { paddingValues ->
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(paddingValues)
                .padding(16.dp)
                .verticalScroll(rememberScrollState())
        ) {

            // API ÌÇ§ ÏÑ§Ï†ï Ïπ¥Îìú
            ApiKeyInputCard(
