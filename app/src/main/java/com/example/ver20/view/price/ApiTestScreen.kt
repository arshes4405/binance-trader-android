                isLoading = isLoading,
                currentTest = currentTest,
                onServerTest = {
                    coroutineScope.launch {
                        isLoading = true
                        currentTest = "ÏÑúÎ≤Ñ Ïó∞Í≤∞"
                        testResults = testResults + "üîÑ Î∞îÏù¥ÎÇ∏Ïä§ ÏÑúÎ≤Ñ Ïó∞Í≤∞ ÌÖåÏä§Ìä∏ Ï§ë..."

                        val (success, message) = binanceApiTester.testServerConnection()
