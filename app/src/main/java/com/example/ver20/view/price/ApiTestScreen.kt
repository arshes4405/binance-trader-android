                        } else {
                            "‚ùå ÏÑúÎ≤Ñ Ïó∞Í≤∞ Ïã§Ìå®: $message"
                        }

                        isLoading = false
                        currentTest = ""
                    }
                },
                onPriceTest = {
                    coroutineScope.launch {
                        isLoading = true
                        currentTest = "Í∞ÄÍ≤© Ï°∞Ìöå"
                        testResults = testResults + "üîÑ BTC Í∞ÄÍ≤© Ï°∞Ìöå ÌÖåÏä§Ìä∏ Ï§ë..."

                        val (success, message) = binanceApiTester.testPriceQuery("BTCUSDT")
                        testResults = testResults + if (success) {
                            "‚úÖ Í∞ÄÍ≤© Ï°∞Ìöå ÏÑ±Í≥µ: $message"
                        } else {
                            "‚ùå Í∞ÄÍ≤© Ï°∞Ìöå Ïã§Ìå®: $message"
                        }

                        isLoading = false
                        currentTest = ""
                    }
                },
                onAuthTest = {
                    if (apiKey.isNotEmpty() && secretKey.isNotEmpty()) {
                        coroutineScope.launch {
                            isLoading = true
                            currentTest = "API Ïù∏Ï¶ù"
                            testResults = testResults + "üîÑ API ÌÇ§ Ïù∏Ï¶ù ÌÖåÏä§Ìä∏ Ï§ë..."

