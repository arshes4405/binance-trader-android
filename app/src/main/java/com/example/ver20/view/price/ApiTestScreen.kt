                        coroutineScope.launch {
                            isLoading = true
                            currentTest = "Ï†ÑÏ≤¥ ÌÖåÏä§Ìä∏"
                            testResults = testResults + "üöÄ Ï†ÑÏ≤¥ ÌÖåÏä§Ìä∏ ÏãúÏûë..."

                            val apiKeyData = ApiKeyData(apiKey, secretKey, isTestnet)
                            val results = binanceApiTester.runFullTest(apiKeyData)

                            results.forEach { (testName, result) ->
                                val (success, message) = result
                                testResults = testResults + if (success) {
                                    "‚úÖ $testName: $message"
                                } else {
                                    "‚ùå $testName: $message"
                                }
                            }

                            testResults = testResults + "üèÅ Ï†ÑÏ≤¥ ÌÖåÏä§Ìä∏ ÏôÑÎ£å!"

                            isLoading = false
                            currentTest = ""
                        }
                    } else {
                        testResults = testResults + "‚ö†Ô∏è API ÌÇ§Î•º Î®ºÏ†Ä ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî"
                    }
                },
                hasApiKeys = apiKey.isNotEmpty() && secretKey.isNotEmpty()
            )

            Spacer(modifier = Modifier.height(16.dp))

            // ÌÖåÏä§Ìä∏ Í≤∞Í≥º Ïπ¥Îìú
            if (testResults.isNotEmpty()) {
                TestResultsCard(
                    results = testResults,
                    onClear = { testResults = emptyList() }
                )
            }

            // Î°úÎî© Ïù∏ÎîîÏºÄÏù¥ÌÑ∞
            if (isLoading) {
