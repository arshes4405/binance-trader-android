                        "‚úÖ API ÌÇ§Í∞Ä ÏïàÏ†ÑÌïòÍ≤å Ï†ÄÏû•ÎêòÏóàÏäµÎãàÎã§"
                    } else {
                        "‚ùå API ÌÇ§ Ï†ÄÏû•Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§"
                    }
                }
            )

            Spacer(modifier = Modifier.height(16.dp))

            // ÌÖåÏä§Ìä∏ Î≤ÑÌäºÎì§ Ïπ¥Îìú
