                            } else {
                                "‚ùå API Ïù∏Ï¶ù Ïã§Ìå®: $message"
                            }

                            isLoading = false
                            currentTest = ""
                        }
                    } else {
                        testResults = testResults + "‚ö†Ô∏è API ÌÇ§ÏôÄ Secret ÌÇ§Î•º Î®ºÏ†Ä ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî"
                    }
                },
                onFullTest = {
