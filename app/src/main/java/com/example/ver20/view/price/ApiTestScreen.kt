                Card(
                    modifier = Modifier.fillMaxWidth(),
                    colors = CardDefaults.cardColors(
                        containerColor = Color(0xFFE1F5FE)
                    )
                ) {
                    Column(
                        modifier = Modifier.padding(16.dp),
                        horizontalAlignment = Alignment.CenterHorizontally
                    ) {
                        CircularProgressIndicator(
                            color = Color(0xFF2196F3)
                        )
                        Spacer(modifier = Modifier.height(8.dp))
                        Text(
                            "üîÑ $currentTest ÏßÑÌñâ Ï§ë...",
                            fontSize = 14.sp,
                            color = Color(0xFF1976D2)
                        )
                    }
                }
            }
        }
    }
}

@Composable
fun ApiKeyInputCard(
    apiKey: String,
    onApiKeyChange: (String) -> Unit,
    secretKey: String,
    onSecretKeyChange: (String) -> Unit,
    showSecretKey: Boolean,
    onToggleSecretKey: () -> Unit,
    isTestnet: Boolean,
    onTestnetChange: (Boolean) -> Unit,
    onSave: () -> Unit
) {
    Card(
        modifier = Modifier.fillMaxWidth(),
        colors = CardDefaults.cardColors(
            containerColor = Color(0xFFE3F2FD)
        )
    ) {
        Column(
            modifier = Modifier.padding(16.dp)
        ) {
            Text(
                "üîë API ÌÇ§ ÏÑ§Ï†ï",
                fontSize = 18.sp,
                fontWeight = FontWeight.Bold,
                color = Color(0xFF1976D2)
            )

            Spacer(modifier = Modifier.height(16.dp))

            OutlinedTextField(
                value = apiKey,
                onValueChange = onApiKeyChange,
                label = { Text("API Key") },
                placeholder = { Text("Î∞úÍ∏âÎ∞õÏùÄ API Key ÏûÖÎ†•") },
                leadingIcon = { Icon(Icons.Default.Key, contentDescription = null) },
                modifier = Modifier.fillMaxWidth(),
                singleLine = true
            )

            Spacer(modifier = Modifier.height(12.dp))

            OutlinedTextField(
                value = secretKey,
                onValueChange = onSecretKeyChange,
                label = { Text("Secret Key") },
                placeholder = { Text("Î∞úÍ∏âÎ∞õÏùÄ Secret Key ÏûÖÎ†•") },
                leadingIcon = { Icon(Icons.Default.Lock, contentDescription = null) },
                trailingIcon = {
                    IconButton(onClick = onToggleSecretKey) {
                        Icon(
                            if (showSecretKey) Icons.Default.Visibility else Icons.Default.VisibilityOff,
                            contentDescription = null
                        )
                    }
                },
                visualTransformation = if (showSecretKey) VisualTransformation.None else PasswordVisualTransformation(),
                modifier = Modifier.fillMaxWidth(),
                singleLine = true
            )

            Spacer(modifier = Modifier.height(12.dp))

            Row(
                verticalAlignment = Alignment.CenterVertically
            ) {
                Switch(
                    checked = isTestnet,
                    onCheckedChange = onTestnetChange
                )
                Spacer(modifier = Modifier.width(8.dp))
                Text(
                    if (isTestnet) "üß™ ÌÖåÏä§Ìä∏ÎÑ∑ Î™®Îìú (ÏïàÏ†Ñ)" else "üî¥ Î©îÏù∏ÎÑ∑ Î™®Îìú (Ïã§Ï†ú)",
                    fontSize = 14.sp,
                    color = if (isTestnet) Color(0xFFFF9800) else Color(0xFFF44336)
                )
            }

            Spacer(modifier = Modifier.height(16.dp))

            Button(
                onClick = onSave,
                modifier = Modifier.fillMaxWidth(),
                enabled = apiKey.isNotEmpty() && secretKey.isNotEmpty()
            ) {
                Icon(Icons.Default.Save, contentDescription = null)
                Spacer(modifier = Modifier.width(8.dp))
                Text("API ÌÇ§ Ï†ÄÏû•")
            }
        }
    }
}

