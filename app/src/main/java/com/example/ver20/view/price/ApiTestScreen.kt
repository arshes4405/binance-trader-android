                secretKey = secretKey,
                onSecretKeyChange = { secretKey = it },
                showSecretKey = showSecretKey,
                onToggleSecretKey = { showSecretKey = !showSecretKey },
                isTestnet = isTestnet,
                onTestnetChange = { isTestnet = it },
